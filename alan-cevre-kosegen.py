# Çağla Nur Sorkun
a=float(input("Uzun Kenar Değerini Giriniz: "))
b=float(input("Kısa Kenar Değerini Giriniz: "))
alan=a*b
çevre=2*(a+b)
köşegen_uzunluğu=(a**2+b**2)**0.5
print("Dikdörtgenin alanı: ",alan)
print("Dikdörtgenin çevresi: ",çevre)
print("Dikdörtgenin köşegen uzunluğu: ",köşegen_uzunluğu)